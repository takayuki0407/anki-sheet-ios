// Packages the built WebView engine (engine-src/dist) into assets/engine.zip and
// writes src/engine/engineVersion.ts with a content hash. The app compares that hash
// against the copy extracted on-device and re-extracts when it changes.
//
// Run `node scripts/bundle-engine.mjs` after building the engine
// (`npm --prefix engine-src run build`), or use `npm run engine:all`.
import {
  readdirSync,
  statSync,
  readFileSync,
  writeFileSync,
  mkdirSync,
  existsSync,
} from "node:fs";
import { resolve, relative, join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { createHash } from "node:crypto";
import { zipSync } from "fflate";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const distDir = resolve(root, "engine-src/dist");
const assetsDir = resolve(root, "assets");
const engineDir = resolve(root, "src/engine");

if (!existsSync(distDir)) {
  console.error(
    "[bundle-engine] engine-src/dist not found. Run `npm --prefix engine-src run build` first.",
  );
  process.exit(1);
}

// Collect every file under dist as { "forward/slash/path": Uint8Array }.
const files = {};
function walk(dir) {
  for (const name of readdirSync(dir)) {
    const full = join(dir, name);
    if (statSync(full).isDirectory()) walk(full);
    else {
      const rel = relative(distDir, full).split("\\").join("/");
      files[rel] = new Uint8Array(readFileSync(full));
    }
  }
}
walk(distDir);

// Content hash (deterministic: sorted paths + bytes) so identical builds keep the
// same version and the device skips re-extraction.
const hash = createHash("sha256");
for (const key of Object.keys(files).sort()) {
  hash.update(key);
  hash.update(files[key]);
}
const version = hash.digest("hex").slice(0, 16);

// Fixed mtime (ZIP dates must be >= 1980) keeps the archive bytes stable across
// rebuilds of identical content; the on-device version check uses the content hash above.
const zipped = zipSync(files, { level: 6, mtime: new Date("2020-01-01T00:00:00Z") });
mkdirSync(assetsDir, { recursive: true });
writeFileSync(resolve(assetsDir, "engine.zip"), zipped);

mkdirSync(engineDir, { recursive: true });
writeFileSync(
  resolve(engineDir, "engineVersion.ts"),
  `// AUTO-GENERATED by scripts/bundle-engine.mjs — do not edit.\nexport const ENGINE_VERSION = "${version}";\n`,
);

console.log(
  `[bundle-engine] ${Object.keys(files).length} files -> assets/engine.zip ` +
    `(${(zipped.length / 1024 / 1024).toFixed(2)} MB), version ${version}`,
);
